from numpy import * 

# six ways of creating array 

# array, linspace,logspace,arange,zeros,ones

arr = array([3,4]) # 1. 
lin = linspace(1,10) # 2. 
log = logspace(1,10) # 3. 
arn = arange(1,10) # 4. 
ze = zeros(10) # 5. 
on = ones(10) # 6. 

print(arr)
print(lin)
print(log)
print(arn)
print(ze)
print(on)
